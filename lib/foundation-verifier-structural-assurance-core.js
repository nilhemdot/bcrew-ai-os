function ensure(checks, condition, check, detail = '') {
  checks.push({ ok: Boolean(condition), check, detail })
}

function evaluateStructuralAssuranceCoreFixture(fixture = {}) {
  const findings = []
  if (fixture.moduleAssuranceClosed !== true) findings.push('module_assurance_hidden_failure')
  if (fixture.backendAssuranceClosed !== true) findings.push('backend_assurance_hidden_failure')
  if (fixture.oldInlinePredicatesRemoved !== true) findings.push('old_structural_assurance_inline_predicates_present')
  return { ok: findings.length === 0, findings }
}

export function buildFoundationVerifierStructuralAssuranceCoreDogfoodProof() {
  const healthy = evaluateStructuralAssuranceCoreFixture({
    moduleAssuranceClosed: true,
    backendAssuranceClosed: true,
    oldInlinePredicatesRemoved: true,
  })
  const rejected = {
    hiddenModuleAssurance: evaluateStructuralAssuranceCoreFixture({
      moduleAssuranceClosed: false,
      backendAssuranceClosed: true,
      oldInlinePredicatesRemoved: true,
    }),
    hiddenBackendAssurance: evaluateStructuralAssuranceCoreFixture({
      moduleAssuranceClosed: true,
      backendAssuranceClosed: false,
      oldInlinePredicatesRemoved: true,
    }),
    oldInlinePredicate: evaluateStructuralAssuranceCoreFixture({
      moduleAssuranceClosed: true,
      backendAssuranceClosed: true,
      oldInlinePredicatesRemoved: false,
    }),
  }
  const ok = healthy.ok && Object.values(rejected).every(result => result.ok === false)
  return {
    ok,
    healthy,
    rejected,
    dogfoodInvariant: ok
      ? 'healthy structural assurance fixture passes; module assurance, backend assurance, and old-inline failures fail closed'
      : 'structural assurance dogfood did not reject every known failure fixture',
  }
}


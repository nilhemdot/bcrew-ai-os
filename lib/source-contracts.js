export function getSourceContracts() {
  return sourceContracts.map(contract => ({
    ...contract,
    actions: (contract.actions || []).map(action => ({ ...action })),
  }))
}

export function getSourceContractsByIds(sourceIds) {
  const wanted = new Set(sourceIds || [])
  return getSourceContracts().filter(contract => wanted.has(contract.sourceId))
}

